const myArray = [ 1, 2, 3, 4, 5, 6, 7, 8, 9, 0 ];

// loop for
// o i = 0 determina o início de onde começa a contagem
// o i < 20 determina até onde vai a contagem
for (let i = 0; i < 20; i ++) {
    console.log(i);
}


// pulando linha
console.log('\n');

